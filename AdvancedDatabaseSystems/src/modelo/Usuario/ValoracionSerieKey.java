package modelo.Usuario;

public class ValoracionSerieKey {
	
	private String nick;
	private int idSerie;
	
	public ValoracionSerieKey(String nick, int idSerie) {
		this.nick = nick;
		this.idSerie = idSerie;
	}

	public String getNick() {
		return nick;
	}

	public int getIdSerie() {
		return idSerie;
	}
	
	

}
